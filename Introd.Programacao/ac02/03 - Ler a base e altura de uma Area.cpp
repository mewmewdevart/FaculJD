// Ler a temperatura em graus Celsius. Calcule e mostre a sua temperatura convertida em fahrenheit
#include <stdio.h>
main() {
	int celsius;
	int fahrenheit;
	
	printf ("Quais sao os graus celsius que voce gostaria de converter para fahrenheit?");
	scanf ("%i", &celsius); 
	
	
	(fahrenheit= celsius *9/5 +32);
	
	
	
	printf ("%i", fahrenheit); 
	
	
	
}
