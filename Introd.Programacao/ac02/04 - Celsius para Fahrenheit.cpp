// Ler os valores inteiros em x e y. Efetua a troca de seus valores e mostre seus conteúdos.

#include <stdio.h>
main() {
	int x;
	int y;
	
	printf ("Qual é o valor de inteiro de x ?");
	scanf ("%i", &x); 
	
	printf ("Qual é o valor de inteiro de y ?");
	scanf ("%i", &y); 
	
		
	x <- y ;
	y <- x ;
	
	
	
	printf ("%i", x ); 
	printf ("%i", y ); 
}
	
